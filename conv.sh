#!/bin/sh -e
n=0
if [ -n "$1" ]; then
	export EXINIT='wq'
fi
for s in *.sh
do
	head -2 "$s" | grep -q "Generated by patch2vi" || continue
	./$s
	new_files=$(git ls-files --others --exclude-standard)
	if [ -n "$new_files" ]; then
		printf '%s\n' $new_files | xargs git add -N
	fi
	./gitdiff2vi.sh -d $s
	git add $s
	if ! git diff --cached --quiet; then
		git commit -m "$s: conv"
		n=$((n+1))
	fi
	git reset --hard
	if [ -n "$new_files" ]; then
		printf '%s\n' $new_files | xargs rm -f
	fi
done

if [ -n "$1" ] && [ $n -gt 0 ]; then
	git reset HEAD~$n
	git add .
fi
