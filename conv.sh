#!/bin/sh -e
for s in *.sh
do
	head -2 "$s" | grep -q "Generated by patch2vi" || continue
	./$s
	new_files=$(git ls-files --others --exclude-standard)
	if [ -n "$new_files" ]; then
		printf '%s\n' $new_files | xargs git add -N
	fi
	./gitdiff2vi.sh -d $s
	git add $s
	git commit -m "$s: conv"
	git reset --hard
	if [ -n "$new_files" ]; then
		printf '%s\n' $new_files | xargs rm -f
	fi
done
