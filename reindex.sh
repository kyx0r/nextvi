#!/bin/sh -e
if [ -f "$1" ]; then
	head -2 "$1" | grep -q "Generated by patch2vi" || continue
	printf "%s\n" "SCRIPT: $1"
	./"$1"
	new_files=$(git ls-files --others --exclude-standard)
	if [ -n "$new_files" ]; then
		printf '%s\n' $new_files | xargs git add -N
	fi
	git diff > /tmp/tmp.patch
	git checkout . &>/dev/null
	if [ -n "$new_files" ]; then
		printf '%s\n' $new_files | xargs git reset HEAD -- &>/dev/null
		printf '%s\n' $new_files | xargs rm -f
	fi
	sed '/^=== PATCH2VI PATCH ===$/q' "$1" > /tmp/tmp.sh
	cat /tmp/tmp.patch >> /tmp/tmp.sh
	cp /tmp/tmp.sh "$1"
	chmod +x "$1"
	git add "$1"
	printf "\n"
	exit 0
fi
for s in *.sh
do
	head -2 "$s" | grep -q "Generated by patch2vi" || continue
	printf "%s\n" "SCRIPT: $s"
	./"$s"
	[ "$1" = "1" ] && ./cbuild.sh build
	new_files=$(git ls-files --others --exclude-standard)
	if [ -n "$new_files" ]; then
		printf '%s\n' $new_files | xargs git add -N
	fi
	git diff > /tmp/tmp.patch
	git checkout . &>/dev/null
	if [ -n "$new_files" ]; then
		printf '%s\n' $new_files | xargs git reset HEAD -- &>/dev/null
		printf '%s\n' $new_files | xargs rm -f
	fi
	# Replace embedded patch: keep everything up to "exit 0", append new diff
	sed '/^=== PATCH2VI PATCH ===$/q' "$s" > /tmp/tmp.sh
	cat /tmp/tmp.patch >> /tmp/tmp.sh
	cp /tmp/tmp.sh "$s"
	chmod +x "$s"
	git add "$s"
	printf "\n"
done
rm -f tmp *.orig *.rej
